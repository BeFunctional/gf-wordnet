static uint32_t
utf8_decode(const char** src_inout)
{
	const uint8_t* src = (const uint8_t*) *src_inout;
	uint8_t c = src[0];
	if (c < 0x80) {
		*src_inout = src + 1;
		return c;
	}
	size_t len = (c < 0xe0 ? 1 :
	              c < 0xf0 ? 2 :
	              c < 0xf8 ? 3 :
	              c < 0xfc ? 4 :
	                         5
	             );
	uint64_t mask = 0x0103070F1f7f;
	uint32_t u = c & (mask >> (len * 8));
	for (size_t i = 1; i <= len; i++) {
		c = src[i];
		u = u << 6 | (c & 0x3f);
	}
	*src_inout = &src[len + 1];
	return u;
}

static int
unicodelen(const char* s)
{
	int len = 0;
	while (utf8_decode(&s) != 0)
		len++;
	return len;
}

static void
transliterate(const char* s, char* t)
{
	for (;;) {
		const char* tmp = s;
		uint32_t ucs = utf8_decode(&s);
		if (ucs == 0) {
			*(t++) = 0;
			break;
		}

		int i = 0;
		int j = sizeof(transl_tbl)/sizeof(transl_tbl[0]) - 1;
		while (i <= j) {
			int k = (i+j)/2;
			if (transl_tbl[k].ucs == ucs) {
				strcpy(t,transl_tbl[k].transl);
				t += strlen(t);
				break;
			} else if (ucs < transl_tbl[k].ucs) {
				j = k-1;
			} else {
				i = k+1;
			}
		}
		
		if (i > j) {
			memcpy(t, tmp, s-tmp);
			t += s-tmp;
		}
	}
}

static void
levenstein(sqlite3_context *context,
           int argc, sqlite3_value **argv)
{
  const char* s1 = sqlite3_value_text(argv[0]);
  const char* s2 = sqlite3_value_text(argv[1]);

  char *t1 = alloca((strlen(s1)+1)*3);
  transliterate(s1, t1);
  s1 = t1;

  char *t2 = alloca((strlen(s2)+1)*3);
  transliterate(s2, t2);
  s2 = t2;

  int len1 = unicodelen(s1);
  int len2 = unicodelen(s2);

  int size = (len1+1)*(len2+1)*sizeof(int);
  int* d = alloca(size);
  memset(d, 0, size);

  // source prefixes can be transformed into empty string by
  // dropping all characters
  for (int i = 1; i <= len1; i++)
    d[i*(len2+1)+0] = i;
 
  // target prefixes can be reached from empty source prefix
  // by inserting every character
  for (int j = 1; j <= len2; j++)
    d[0*(len2+1)+j] = j;

  const char* p2 = s2;
  for (int j = 1; j <= len2; j++) {
	uint32_t c2 = utf8_decode(&p2);
	
	const char* p1 = s1;
    for (int i = 1; i <= len1; i++) {
      uint32_t c1 = utf8_decode(&p1);

	  int subst = (c1 == c2) ? 0 : 1;

      int r, min = INT_MAX;
      r = d[(i-1)*(len2+1)+j    ] + 1;      if (r < min) min = r;
      r = d[i    *(len2+1)+(j-1)] + 1;      if (r < min) min = r;
      r = d[(i-1)*(len2+1)+(j-1)] + subst;  if (r < min) min = r;
      d[i*(len2+1)+j] = min;
	}
  }

  sqlite3_result_int(context, d[(len1+1)*(len2+1)-1]);
}

